import java.util.*;
public class BucketFilling_58 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		int t=sc.nextInt();
		while(t-->0)
		{
			int k=sc.nextInt();
			int x=sc.nextInt();
			int rem=k-x;
			System.out.println(rem);
		}
	}
}
