package java100days;
import java.util.*;
//Day 27/100
// Double of number without using arithmetic operator
public class DoubleNo_27 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the program:");
		int num=sc.nextInt();
		System.out.println("Double of number without using arithmetic operator");
		System.out.println(num<<1);
	
	}
}
